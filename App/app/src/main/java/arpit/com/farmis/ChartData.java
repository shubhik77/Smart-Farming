package arpit.com.farmis;

public class ChartData {
    float x;
    float y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public ChartData(float x, float y) {

        this.x = x;
        this.y = y;
    }

    public ChartData() {

    }
}
